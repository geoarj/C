#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*p;
	int		i;

	i = 0;
	p = (char*)malloc(ft_strlen((char *)str) + 1);
	if (!p)
		return (NULL);
	while (str[i] != '\0')
	{
		p[i] = str[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
